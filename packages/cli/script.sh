# 无限循环执行
while true; do
    for command in "${commands[@]}"; do
        # 并行执行每个命令，并将其放入后台
        $command &

        # 检查命令是否成功执行
        if [ $? -ne 0 ]; then
            echo "命令执行失败，退出循环"
            exit 10
        fi
    done

    # 等待所有并行任务完成
    wait

    # 等待 12 秒后继续执行
    sleep 3
done
